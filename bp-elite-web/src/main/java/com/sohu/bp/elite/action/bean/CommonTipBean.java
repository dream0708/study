package com.sohu.bp.elite.action.bean;

public class CommonTipBean {
    private String tipType;
    private String tipTitle;
    private String tipContent;
    
    public String getTipType() {
        return tipType;
    }
    public CommonTipBean setTipType(String tipType) {
        this.tipType = tipType;
        return this;
    }
    public String getTipTitle() {
        return tipTitle;
    }
    public CommonTipBean setTipTitle(String tipTitle) {
        this.tipTitle = tipTitle;
        return this;
    }
    public String getTipContent() {
        return tipContent;
    }
    public CommonTipBean setTipContent(String tipContent) {
        this.tipContent = tipContent;
        return this;
    }
    
    
}
